char * getAutomorphicNumerByLength(char output[], int len){
	int i, pos;
	for(pos = 0, i=genLen - len; i < genLen; i++, pos++){
		output[pos] = generator[i];
	}
	output[pos] = '\0';
	return output;
}

char * twinAutomorphicNumByLength(char output[], int len){
	int i, carry = 0;
	for(i=0; i<len; i++){
		output[i] = '9' - output[i];
	}
	for(i=len-1; i>=0; i--){
		output[i] = output[i] + 2 + carry;
		carry = output[i] / 10;
		output[i] = output[i] % 10;
		if(carry == 0){
			break;
		}
	}
	for(i=len-1; i>=0; i--){
		output[i] += '0';
	}
	return output;
}

